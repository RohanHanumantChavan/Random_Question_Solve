   #include<stdio.h>
   double CircleArea(float Radius)
   {
      float PI = 3.14;
      double Area = 0.0;

      Area = PI * Radius * Radius;

      return Area;

}
   int main()
   {
      float fValue = 0.0f;
      double dRet = 0.0f;

      printf("Enter Radius\n");
      scanf("%f",&fValue);
      dRet = CircleArea(fValue);
      printf("Area of Circle is :%lf",dRet);
      return 0;
   }