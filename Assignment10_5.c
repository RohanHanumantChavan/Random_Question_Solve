   #include<stdio.h>
   double FindSquareMeter(int iNo)
   {
      float Square = 0.0929;
      double SquareMeter = 0.0;

      SquareMeter = iNo * Square;

      return SquareMeter;

}
   int main()
   {
      int iValue = 0;
      double dRet = 0.0;

      printf("Enter Area in Square feet :\n");
      scanf("%d",&iValue);

      dRet = FindSquareMeter(iValue);
      printf("Square in Meter is :%lf",dRet);
      return 0;
   }