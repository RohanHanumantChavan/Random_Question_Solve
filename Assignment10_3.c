   #include<stdio.h>
   #define Meter 1000
   int KMToMeter(int iNo)
   {
      if(iNo < 0)
      {
         iNo = -iNo;
      }
      int iMeter = 0;
      iMeter = iNo * Meter;

      return iMeter;

}
   int main()
   {
      int iValue = 0; 
      int iRet = 0;

      printf("Enter distance: \n");
      scanf("%d",&iValue);

      iRet = KMToMeter(iValue);

      printf("distance in Meter is :%d",iRet);
      return 0;
   }