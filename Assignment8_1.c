   #include<stdio.h>
   void CheckNumber(int iNo)
   {
      if(iNo < 50)
      {
        printf("%d Number is Small",iNo);
      }
      if((iNo > 50) && (iNo < 100))
      {
          printf(" %d Number is Medium",iNo);
      }

      if(iNo > 100)
      {
        printf("  %d Number is greater",iNo);
      }
   }
   int main()
  {
    int iValue = 0;

    printf("Enter the number \n");
    scanf("%d",&iValue);

    CheckNumber(iValue);
    return 0;
  }  
