   #include<stdio.h>
   int DisplayFactorial(int iNo)
   {
    if(iNo < 0)
    {
      iNo = -iNo;
    }
      int iCnt = 0, iFact = 1;
      for(iCnt = 1; iCnt <= iNo; iCnt++)
      {
        iFact = iFact * iCnt;
      }
      return iFact;
     
   }
   int main()
  {
    int iValue = 0, iRet = 0;

    printf("Enter the Number \n");
    scanf("%d",&iValue);

    iRet = DisplayFactorial(iValue);
    printf("Factorial of %d is :  %d\n",iValue,iRet);

    
    return 0;
  }  
