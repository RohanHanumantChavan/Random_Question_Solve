   #include<stdio.h>
   int DollarToINR(int iNo)
   {
    int iINR = 0;
    if(iNo < 0)
    {
      iNo = -iNo;
    }
       if(iNo != 0)
       { 
          iINR = iNo * 70; 
           
       }
       return iINR;
   }
   int main()
  {
    int iValue = 0;
    int iRet = 0;
    printf("Enter the Number of USD\n");
    scanf("%d",&iValue);

     iRet = DollarToINR(iValue);
     printf("Value int INR is %d\n",iRet);
     return 0;
  }  
