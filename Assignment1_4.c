typedef int BOOL;
#define TRUE 1
#define FALSE 0
BOOL CheckDivisibleByFive(int iNo)
{
    if((iNo % 5)== 0)
    {
        return TRUE;
    }
    else
    {
        return FALSE;
    }
}
int main()
{
    int iValue = 0;
    BOOL iRet = FALSE;

    printf("Enter the number\n");
    scanf("%d",&iValue);

    iRet = CheckDivisibleByFive(iValue);
    if(iRet == TRUE)
    {
        printf("Number is divisible by 5");
    }
    else
    {
        printf("Number is not Divisible by 5");
    }
    return 0;
}
