   #include<stdio.h>
   void DisplayTableReverse(int iNo)
   {
    if(iNo < 0)
    {
      iNo = -iNo;
    }
      int iCnt = 0;
      printf(" Table of %d is : \n",iNo);
      for(iCnt = 10; iCnt >= 1; iCnt--)
      {
        printf("%d\t",iNo * iCnt);
      }
      
     }
   int main()
  {
    int iValue = 0;

    printf("Enter the Number \n");
    scanf("%d",&iValue);

     DisplayTableReverse(iValue);
   

    
    return 0;
  }  
