 #include<stdio.h>
 
 int CountTwo(int iNo)
 {
   int iDigit = 0;
   int iCount = 0;

   if(iNo < 0)
   {
      iNo = -iNo;
   }
   while(iNo != 0)
   {
      iDigit = iNo % 10;
      if(iDigit == 2)
      {
         iCount++;
      }
     
       iNo = iNo / 10;

       
   }
   return iCount;

 }
