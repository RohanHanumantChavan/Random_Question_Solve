///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

 typedef int BOOL;
 #define TRUE 1
 #define FALSE 0

 BOOL CheckGreater(int iNo)
{
  if(iNo > 100)
  {
    return TRUE;
  }
  else
  {
    return FALSE;
  }


}
int main()
{
  int iValue = 0;
  BOOL bRet = FALSE;

  printf("Enter the number:\n");
  scanf("%d",&iValue);

  bRet = CheckGreater(iValue);
  if(bRet == TRUE)
  {
    printf("%d number is greater than 100 :",iValue);
  }
  else
  {
    printf("%d Number is smaller than 100 :",iValue);
  }

    return 0;
}



